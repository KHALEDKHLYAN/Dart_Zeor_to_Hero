/*
ALso Comparison Operators. They're Used to make Comparison
 Examples 
 > (Greater than)
 < (Less than or equal to)
 >= (Greator than or Equal to)
 <= (Less than or Equal to)
 == (Equal to)
 != (Not Equal to)

 */

void main() {
  int valu1 = 30;
  int number = 15;

  print(valu1 > number);
  print(valu1 < number);
  print(valu1 >= number);
  print(valu1 <= number);
  print(valu1 == number);
  print(valu1 != number);
}
